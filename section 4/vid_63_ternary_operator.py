"""
Ternary Operator

condition_if_true if condition else condition_if_else
"""
print('-----------------------------------')
is_friend = True
can_message = 'allowed to message' if is_friend else 'not allowed to message'
print(can_message)
print('-----------------------------------')
print('-----------------------------------')
is_friend = False
can_message = 'allowed to message' if is_friend else 'not allowed to message'
print(can_message)
print('-----------------------------------')
